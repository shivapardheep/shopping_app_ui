class Dress {
  String name;
  String brand;
  double starRating;
  int reviewCount;
  String offer;
  String description;
  List<String> sizes;
  double price;
  String imageUrl;

  Dress({
    required this.name,
    required this.brand,
    required this.starRating,
    required this.reviewCount,
    required this.offer,
    required this.description,
    required this.sizes,
    required this.price,
    required this.imageUrl,
  });
}
